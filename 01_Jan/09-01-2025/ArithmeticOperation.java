public class ArithmeticOperation{
	public static void main(String arg[]){
	
		int a = 10;
		int b = 5;
		
		int add = a+b;
		int mult = a*b;
		int sub = a-b;
		int div = a/b;
		int mod = a%b;
		
		System.out.println("Addition of 10 & 5 is = "+add);
		System.out.println("multiplication of 10 & 5 is = "+mult);
		System.out.println("Subtraction of 10 & 5 is = "+sub);
		System.out.println("Division of 10 & 5 is = "+div);
		System.out.println("Modulus division of 10 & 5 is = "+mod);
		
	}
}